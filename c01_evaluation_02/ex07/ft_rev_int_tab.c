void	ft_rev_int_tab(int *tab, int size)
{
	int	hold;
	int	i;

	i = 0;
	while (2 * i <= size - 1)
	{
		hold = *(tab + i);
		*(tab + i) = *(tab + size - i - 1);
		*(tab + size - i - 1) = hold;
		i++;
	}
}
